/**
 * Este es un tutorial de Express y Node.js, esta basado enteramente del siguiente manual:
 * https://www.tutorialspoint.com/expressjs/
 * Para poder seguir este tutorial, se necesita tener instalado npm asi como node.js
 * ambos pueden ser descargados de sus respectivas paginas
 * 
 * Tras ello, en una carpeta vacia haremos npm init, lo que inicializara el proyecto y nos pedira algunos
 * datos, podemos ir dando a enter, no son importantes ahora, pero tened en cuenta que el nombre del proyecto
 * debe ser en minusculas
 * Una vez echo esto, lo que haremos sera instalar el paquete express, que es el que usaremos durante este manual
 * para ello, utilizaremos el comando npm --install express --save
 * con el --save, indicamos que queremos que se nos guarde como dependencia en nuestro archivo package.json, con lo
 * que cuando lo subamos a un repositorio, no necesitaremos subir tambien la carpeta node_modules, y el que lo descargue
 * solo tendra que poner npm --install, instalando de esta manera todos los paquetes necesarios.
 * 
 * Para terminar, hablaremos de express, express, es un framework minimalista y flexible que provee de una gran set de
 * recursos para aplicaciones web y mobiles, nos permite construir websites, aplicaciones web y back end de una forma simple,
 * sin tener que preocuparnos de protocolos de bajo nivel, procesos, etc...
 * Frente a los competidores, no tiene una mejor manera de hacer las cosas, ya que es realemente flexible y plugable.
 * Funciona muy bien con el sistema de base de datos MongoDB, que es un sistema de dessarrollo y gestion de base de datos 
 * basadas en documentos, es decir NoSQL. Es codigo abierto.
 * 
 * Pug, antes conocido como Jade, es un breve lenguaje para escribir platillas HTML, sus funciones son:
 * - Producir HTML
 * - Soporta codigo dinamico
 * - Soporta reusabilidad
 * Es uno de los lenguajes de plantillas mas usados con Express.
 * Mongoose, es la api de MongoDB para node y nos permite acceder a ella de una manera mas sencilla.
 */
